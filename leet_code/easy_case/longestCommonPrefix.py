class Solution:
    def commonPrefix(self, a, b):
        """
        type: str
        rtype: str
        """
        la = len(a)
        lb = len(b)
        l = min(la, lb)
        ret = ""
        if a[0] != b[0]:
            return ret
        else:
            ret += a[0]

        for i in range(1, l):
            if a[i] == b[i]:
                ret += a[i]
            else:
                break

        return ret

    def longestCommonPrefix(self, strs) -> str:
        """
        type: list
        rtype: str
        """
        if len(strs) < 1:
            return ""

        prefix = ""
        # print('strs:', strs)
        # print('len(set(strs)) -----------------', len(set(strs)))
        if len(set(strs)) == 1:
            # print('strs: enter -----------------', strs)
            return strs[0]
        if "" in strs:
            return ""
        head_list1 = [i[0] for i in strs]
        if len(set(head_list1)) > 1:
            return ""
        # judge the prefix per pare
        pref_list = []
        for i in range(0, len(strs)):
            for j in range(i + 1, len(strs)):
                # print('i:{0}, j:{1}'.format(i,j))
                # print('strs[i]:{0}, strs[j]:{1}'.format(strs[i],strs[j]))
                pref_list.append(self.commonPrefix(strs[i], strs[j]))

        # print('pref_list: ', pref_list)
        len_list = [len(i) for i in pref_list]
        min_len = min(len_list)
        print("output:", pref_list[0][:min_len])
        return pref_list[0][:min_len]


